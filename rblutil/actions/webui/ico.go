package webui

import (
	"bytes"
	"encoding/base64"
	"io/ioutil"
)

func decode(content []byte) []byte {
	a := bytes.NewReader(content)
	ac := base64.NewDecoder(base64.StdEncoding, a)
	data, err := ioutil.ReadAll(ac)
	if err != nil {
		panic(err)
	}
	return data
}

